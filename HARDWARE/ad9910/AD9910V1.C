{ ulong Temp;
	
	Temp = 1000000000/(unsigned long int)Sample_interval/4/64; //1GHz/4, 采样间隔范围：4*(1~65536)ns
	if(Temp > 0xffff)
		Temp = 0xffff;
	ramprofile0[7] = 0x24;
	ramprofile0[6] = 0x00;
	ramprofile0[5] = 0x00;
	ramprofile0[4] = 0xc0;
	ramprofile0[3] = 0x0f;
	ramprofile0[2] = (uchar)Temp;
	ramprofile0[1] = (uchar)(Temp>>8);
	ramprofile0[0] = 0x00;
	Txramprofile();
  Txramdata0(0);
  cfr1[0] = 0xc0; //RAM 使能，幅度控制
	cfr2[1] = 0x00; //DRG 失能
	Txcfr(); //发送cfrx控制字	
 }

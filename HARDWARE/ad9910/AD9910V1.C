//======================发送8位数据程序===================================
void txd_8bit(uchar txdat)
{uchar i,sbt;
  sbt=0x80;
  SCLK=0;
  for (i=0;i<8;i++)
   {if ((txdat & sbt)==0) AD9910_SDIO=0; else AD9910_SDIO=1;
    SCLK=1;
	sbt=sbt>>1;
	SCLK=0;
   }
 }    

